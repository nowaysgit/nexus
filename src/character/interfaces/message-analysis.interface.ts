/**
 * Интерфейс для результата анализа сообщения пользователя
 */
export interface MessageAnalysis {
  /**
   * Влияние сообщения на различные потребности персонажа
   * Значения от -10 до +10, где отрицательные значения означают удовлетворение потребности,
   * а положительные - усиление потребности
   */
  needsImpact: {
    [needType: string]: number;
  };

  /**
   * Эмоциональная реакция персонажа на сообщение
   */
  emotionalReaction: string;

  /**
   * Важность сообщения для персонажа
   * Значение от 0 до 1, где 0 - совсем не важно, 1 - крайне важно
   */
  importance: number;

  /**
   * Требуется ли персонажу изменить текущее действие
   */
  requiresActionChange: boolean;

  /**
   * Предлагаемое действие, если requiresActionChange = true
   */
  suggestedAction?: string;
}
